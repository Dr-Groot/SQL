/* The MIN() function returns the smallest value of the selected column.
  The MAX() function returns the largest value of the selected column. */
  
/* MIN() SYNTAX */
SELECT MIN(column_name)
FROM table_name
WHERE condition;

/* MAX() SYNTAX */
SELECT MAX(column_name)
FROM table_name
WHERE condition;

